// for loop
for(let i = 0; i<=10; i++){
    console.log(i);
}


// while loop
let number = 0;
while(number<=10){
    console.log(number);
    number++;
}

// do while loop 
let num1 = 0;
do{
    console.log(num1);
    num1++;
}while(num1<=10);

